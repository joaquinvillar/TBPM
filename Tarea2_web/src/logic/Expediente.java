package logic;

public class Expediente {

	private String movilidadNombre;
	private String convocatoriaId;
	private String fechaInicio;
	private String fechaFin;
	private String docenteRef;
	private String universidad;
	private String bases;
	private String descripcion;
	
	
	public Expediente() {
	}
	public String getMovilidadNombre() {
		return movilidadNombre;
	}
	public void setMovilidadNombre(String movilidadNombre) {
		this.movilidadNombre = movilidadNombre;
	}
	public String getConvocatoriaId() {
		return convocatoriaId;
	}
	public void setConvocatoriaId(String convocatoriaId) {
		this.convocatoriaId = convocatoriaId;
	}
	public String getFechaInicio() {
		return fechaInicio;
	}
	public void setFechaInicio(String fechaInicio) {
		this.fechaInicio = fechaInicio;
	}
	public String getFechaFin() {
		return fechaFin;
	}
	public void setFechaFin(String fechaFin) {
		this.fechaFin = fechaFin;
	}
	public String getDocenteRef() {
		return docenteRef;
	}
	public void setDocenteRef(String docenteRef) {
		this.docenteRef = docenteRef;
	}
	public String getUniversidad() {
		return universidad;
	}
	public void setUniversidad(String universidad) {
		this.universidad = universidad;
	}
	public String getBases() {
		return bases;
	}
	public void setBases(String bases) {
		this.bases = bases;
	}
	public String getDescripcion() {
		return descripcion;
	}
	public void setDescripcion(String descripcion) {
		this.descripcion = descripcion;
	}
	
	
}
